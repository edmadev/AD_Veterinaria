    /**
     * Iniicia la tabla con el nñumero de vacunas , de orden mayor a menor.
     */
   private void iniciarTablaMasVacunas() {
        ConsultasPaciente consulta = new ConsultasPaciente();
        DefaultTableModel modelo = new DefaultTableModel();
        ArrayList<Paciente> listaMasVacunados = consulta.topMasVacunas(returnLimite());

        modelo.addColumn("ID Mascota");
        modelo.addColumn("Alias Mascota");
        modelo.addColumn("Vacunaciones");

        for (Paciente paciente : listaMasVacunados) {
            modelo.addRow(new Object[]{
                paciente.getIdMascota(),
                paciente.getAliasMascota(),
                paciente.getVacunaciones()
            });
        }

        tbTabla.setModel(modelo);
    }
     
   /**
    * Método que establece el límite de registros a mostrar.
    * @return El límite elegido por el usuario.
    */
     private int returnLimite(){
        int limite = Integer.parseInt(txtLimite.getText());
        return limite;
        
    }

